public class Notes{
    public static void main(String[] args) {
        
        // equivalent assignment operators
        double x = 0;
        x = x + 4;
        x += 4;

        x = x - 3;
        x -= 3;

        x = x * 10;
        x *= 10;

        x = x / 2;
        x /= 2;

        x = x % 4;
        x %= 4;

    }
}